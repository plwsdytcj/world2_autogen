type I18nContextType = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
};

const I18nContext = React.createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = React.useState<Lang>('en');

  React.useEffect(() => {
    const saved = localStorage.getItem('lang') as Lang | null;
    if (saved && (saved === 'en' || saved === 'zh' || saved === 'ja')) {
      setLang(saved);
      return;
    }
    // Detect from browser if not explicitly saved
    try {
      const pick = (s?: string | null): Lang => {
        const v = (s || '').toLowerCase();
        if (v.startsWith('zh')) return 'zh';
        if (v.startsWith('ja')) return 'ja';
        return 'en';
      };
      const nav = typeof navigator !== 'undefined' ? navigator : ({} as any);
      const langs: string[] = Array.isArray((nav as any).languages) ? (nav as any).languages : [];
      const chosen = langs.length > 0 ? pick(langs[0]) : pick((nav as any).language);
      setLang(chosen);
    } catch {
      setLang('en');
    }
  }, []);

  const t = React.useMemo(() => {
    const table = dicts[lang] || dicts.en;
    return (key: string) => table[key] ?? key;
  }, [lang]);

  const value = React.useMemo<I18nContextType>(() => ({
    lang,
    setLang: (l) => {
      localStorage.setItem('lang', l);
      setLang(l);
    },
    t,
  }), [lang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

// Default fallback for when context is not available
const defaultI18n: I18nContextType = {
  lang: 'en',
  setLang: () => {},
  t: (key: string) => dicts.en[key] ?? key,
};

// Check if React's dispatcher is available (we're inside a component render)
function isReactReady(): boolean {
  try {
    // Access React internals to check if dispatcher is ready
    // This is a hack but necessary to avoid the "hooks called outside component" error
    const internals = (React as any).__SECRET_INTERNALS_DO_NOT_USE_OR_YOU_WILL_BE_FIRED;
    return internals?.ReactCurrentDispatcher?.current != null;
  } catch {
    return false;
  }
}

export function useI18n(): I18nContextType {
  // If React isn't ready (called outside component), return default
  if (!isReactReady()) {
    return defaultI18n;
  }
  
  const ctx = React.useContext(I18nContext);
  return ctx ?? defaultI18n;
}
